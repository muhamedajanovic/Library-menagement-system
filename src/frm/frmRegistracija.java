    public boolean provjeraMaila(String mail){
        String sql = "SELECT `email` FROM `korisnici` WHERE email = ?";
            try {
                kon = DBConnect.uspostaviKonekciju();
                ps = kon.prepareStatement(sql);
                ps.setString(1, mail);
                rs = ps.executeQuery();
                
                if(rs.next()){
                    return false;
                }
                return true;
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Greška u komunikaciji sa serverom -> "+e.getMessage());
                return false;
            }
    }; 
    
    public void popunjavanjePolja(JTextField trenutno, JTextField sljedece, java.awt.event.KeyEvent evt){
        trenutno.setBackground(Color.white);
        int key = evt.getKeyCode();
        if(key==10){
            if(trenutno.getText().isEmpty()){
                trenutno.requestFocus();
                trenutno.setBackground(Color.red);
            } else {
                sljedece.requestFocus();
            }
        }
    }
    
    private void btnRegistracijaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistracijaActionPerformed
        // TODO add your handling code here:
        imeTxtBox.setBackground(Color.white);
        prezimeTxtBox.setBackground(Color.white);
        emailTxtBox.setBackground(Color.white);
        sifraTxtBox.setBackground(Color.white);
        sifra2TxtBox.setBackground(Color.white);
               
        String ime = imeTxtBox.getText();
        String prezime = prezimeTxtBox.getText();
        String email = emailTxtBox.getText();
        String sifra = sifraTxtBox.getText();
        String sifra2 = sifra2TxtBox.getText();
        
        Random random = new Random();
        int OTP = random.nextInt(900000) + 100000;
        
        from = "softverskotest@gmail.com";
        to = email;
        host = "localhost";
        sub = "Verifikacija registracije za biblioteku";
        content = "Vas kod za verifikaciju za biblioteku je " + String.valueOf(OTP) + " Unesite ga u formu za verifikaciju kako biste potvrdili registraciju";
        
        Properties p = new Properties();
        p.put("mail.smtp.auth", "true");
        p.put("mail.smtp.starttls.enable", "true");
        p.put("mail.smtp.host", "smtp.gmail.com");
        p.put("mail.smtp.port", "587");
        
        Session s = Session.getDefaultInstance(p, new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication(){
                return new PasswordAuthentication("softverskotest@gmail.com", "hciadmguqihvylsi");
            }
        
        });       
        
        if(validacijaUnosa(ime, prezime, sifra, sifra2, email)&&podudarnostSifri(sifra, sifra2)&&provjeraMaila(email)){
            try {
                MimeMessage m = new MimeMessage(s);
                m.setFrom(from);
                m.addRecipient(Message.RecipientType.TO, new InternetAddress(to));
                m.setSubject(sub);
                m.setText(content);
            
                Transport.send(m);
                
                String sql = "INSERT INTO verifikacije(mail, OTP) VALUES (?,?)";
                try {
                    kon = DBConnect.uspostaviKonekciju();
                    ps = kon.prepareStatement(sql);
                    ps.setString(1, email);
                    ps.setString(2, String.valueOf(OTP));
                    ps.executeUpdate();
                    this.setVisible(false);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Greška u komunikaciji sa serverom -> "+e.getMessage());
                }
                
                new frmVerifikacija().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            if(!validacijaUnosa(ime, prezime, sifra, sifra2, email)){
                if(ime.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Polje za ime je prazno");
                    imeTxtBox.requestFocus();
                    imeTxtBox.setBackground(Color.red);
                } else if (prezime.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Polje za prezime je prazno");
                    prezimeTxtBox.requestFocus();
                    prezimeTxtBox.setBackground(Color.red);
                } else if (sifra.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Polje za sifru je prazno");
                    sifraTxtBox.requestFocus();
                    sifraTxtBox.setBackground(Color.red);
                } else if (sifra2.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Ponovi sifru");
                    sifra2TxtBox.requestFocus();
                    sifra2TxtBox.setBackground(Color.red);
                } else if (email.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Polje za email je prazno");
                    emailTxtBox.requestFocus();
                    emailTxtBox.setBackground(Color.red);
                }
            }
            else if (!podudarnostSifri(sifra, sifra2)){
                JOptionPane.showMessageDialog(null, "Sifre se ne podudaraju");
                sifraTxtBox.requestFocus();
                sifraTxtBox.setBackground(Color.red);
                sifra2TxtBox.setBackground(Color.red);
            }
            else if (!provjeraMaila(email)){
                JOptionPane.showMessageDialog(null, "Mail je vec u upotrebi");
                emailTxtBox.requestFocus();
                emailTxtBox.setBackground(Color.red); 
            }
        }
        
        
        
    }//GEN-LAST:event_btnRegistracijaActionPerformed

