    public static boolean validacijaUnosa(String ime, String prezime, String sifra, String sifra2, String email){
        if (ime.isEmpty() || prezime.isEmpty() || sifra.isEmpty() || sifra2.isEmpty() || email.isEmpty()) {
            return false;
        }
        return true;
    };
    
    public static boolean podudarnostSifri(String sifra, String sifra2){
        if (sifra.equals(sifra2)) {
            return true;
        }
        return false;
    }; 
